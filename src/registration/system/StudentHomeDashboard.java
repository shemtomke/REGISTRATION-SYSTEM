    void DisplayUnitsRegistered()
    {
        try {
            String registeredUnits = "SELECT UnitID, UnitName, Status FROM unitregistration "
                    + "WHERE RegNO = (SELECT RegNO FROM student WHERE RegNO = '" + userDetails.getUsername() + "')";
            
            ps = ConnectionDatabase.DbConnection().prepareStatement(registeredUnits);
            rs = ps.executeQuery();
            
            DefaultTableModel tableModel = (DefaultTableModel) unitsTable.getModel();
            
            while(rs.next())
            {
                String unitCode = rs.getString("UnitID");
                String unitName = rs.getString("UnitName");
                String status = rs.getString("Status");
                
                tableModel.addRow(new Object[]{unitCode, unitName, status});
            }
        } catch (Exception e) {
        }
    }
    
    public void DisplayStudent()
    {
        StudentLogin login = new StudentLogin();
        
        //GET NAME, YEAR, SCHOOL, PROGRAMME
        try 
            {
                //Fetch Values for the specific user
String studentDetails = "SELECT student.FullName, student.RegNO, course.coursename, school.schoolname, semester.semname "
                + "FROM student "
                + "INNER JOIN course ON student.Courseid = course.courseid "
                + "INNER JOIN school ON student.schoolid = school.schoolid "
                + "INNER JOIN semester ON student.semid = semester.semid "
                + "WHERE RegNO = '" + userDetails.getUsername() + "'";

                ps = ConnectionDatabase.DbConnection().prepareStatement(studentDetails);
                
                rs = ps.executeQuery();
                        
                while(rs.next())
                {     
                    String fullName = rs.getString("FullName");
                    String regNO = rs.getString("RegNO");
                    String course = rs.getString("CourseName");
                    String school = rs.getString("SchoolName");
                    String sem = rs.getString("SemName");
                    
                    studentNameLbl.setText(fullName);
                    regNumberLbl.setText(regNO);
                    semLbl.setText(sem);
                    courseLbl.setText(course);
                    schoolLbl.setText(school);
                    
                    semester = sem;
                    semID = rs.getString("SemID");
                    courseID = rs.getString("CourseID");
                }
            }
            catch (Exception e)
            {
                
            }
    }
    
